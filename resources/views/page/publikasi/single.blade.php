        <p>Jakarta - Kementerian Pekerjaan Umum dan Perumahan Rakyat (PUPR) bersama mitra kerja Badan Usaha Jalan Tol (BUJT) terus bekerja sama melanjutkan pembangunan jalan tol dan meningkatkan kualitas pengelolaan jalan tol dalam rangka peningkatan konektivitas di seluruh wilayah Indonesia. </p>
        <p>“Hingga Maret 2023 terdapat 70 ruas jalan tol operasi yang dikelola oleh 49 BUJT dengan total panjang jalan tol 2.623,51 km. Ditargetkan hingga akhir 2024 akan tersambung jalan tol sepanjang total 3.196 km, dimana jalan tol yang saat ini dalam tahap konstruksi sepanjang 720 km,” kata Kepala Badan Pengatur Jalan Tol (BPJT) Kementerian PUPR Danang Parikesit pada Rapat Dengar Pendapat (RDP) dengan Komisi V DPR RI, Selasa (28/3/2023).</p>
        <p>Danang menjelaskan, jalan tol yang sudah terbangun dan mulai beroperasi sepanjang tahun 2023 ini sepanjang 24,39 km yaitu Semarang-Demak Seksi 2 (16,01 km), Bekasi-Cawang-Kp Melayu seksi 2A (4,88 km), dan Cinere-Jagorawi seksi 3A (3,5 km). Selanjutnya, direncanakan hingga akhir 2023 jalan tol beroperasi akan bertambah 309,78 km, yaitu Cileunyi-Sumedang-Dawuan seksi 4-6 (28,2 km), Ciawi-Sukabumi seksi 2 (11,9 km), Cibitung-Cilincing seksi 4 (7,7 km), Cimanggis-Cibitung seksi 2 (23,01 km), Serpong-Cinere seksi 2 (3,6 km), Sigli-Banda Aceh seksi 5-6 (13,2 km), Kuala Tanjung-Tebing Tinggi-Parapat seksi 1-2 (38,45 km), Pasuruan-Probolinggo seksi 4A (8,57 km), Jakarta-Cikampek II Selatan Paket 3 (31,25 km), Serpong-Balaraja seksi 1B (5,4 km), Kisaran-Tebing Tinggi (47,6 km), Binjai-Langsa (26,2 km), dan Sp. Indralaya-Prabumulih (64,7 km). </p>
        <p>Sedangkan pada tahun 2024 ditargetkan sepanjang 262,41 km jalan tol akan beroperasi, yaitu Kayu Agung-Palembang-Betung (24,9 km), Padang-Pekanbaru (24,7 km), Cinere-Jagorawi seksi 3 (2 km), Sigli-Banda Aceh seksi 1 (24,67 km), Kuala Tanjung-Tebing Tinggi-Parapat seksi 3-4 (58 km), Serang-Panimbang seksi 2 (24,17 km), Solo-Yogyakarta-NYIA Kulonprogo Paket 1-2 (42,37 km), Yogyakarta-Bawen seksi 1 (8,8 km), dan Jalan Tol Akses IKN (52,8 km). </p>
        <p>“Sebagai upaya pengelolaan jalan tol berkelanjutan, Kementerian PUPR terus mendorong BUJT untuk terus meningkatkan kualitas dan estetika jalan tol dengan beautifikasi dan landscaping, serta memperhatikan pemenuhan Standar Pelayanan Minimum (SPM) sebagaimana instruksi Menteri PUPR,” ujar Danang.</p>
        <p>Dalam rangka meningkatkan fungsi monitoring penyelenggaraan jalan tol, Danang menyampaikan bahwa telah dilakukan juga pengembangan teknologi, salah satunya penerapan sistem transaksi tol non tunai nirsentuh (MLFF) dengan memanfaatkan teknologi Global Navigation Satelite System (GNSS) sehingga dapat menghilangkan antrean di gerbang tol. MLFF ditargetkan akan diujicoba di Jalan Tol Bali-Mandara pada Juni 2023.</p>
        <p>Terakhir, Danang mengungkapkan bahwa masih ada beberapa kendala penyelenggaraan jalan tol, di antaranya keterbatasan fiskal pemerintah serta penurunan kemampuan investor akibat kondisi perekonomian global.  </p>
